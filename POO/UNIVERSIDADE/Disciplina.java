package POO.UNIVERSIDADE;

class Disciplina {
    private String nome;
    private int semestre;

    public Disciplina() {

    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getSemestre() {
        return semestre;
    }
    public void setSemestre(int semestre) {
        this.semestre = semestre;
    }
}
